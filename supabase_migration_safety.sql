-- Create Safety Reports Table
CREATE TABLE IF NOT EXISTS safety_reports (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID NOT NULL,
  report_number TEXT NOT NULL,
  location TEXT NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- Create Safety Non Conformities Table
CREATE TABLE IF NOT EXISTS safety_non_conformities (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  report_id BIGINT REFERENCES safety_reports(id) ON DELETE CASCADE,
  description TEXT NOT NULL,
  suggestion TEXT NOT NULL,
  normative_items TEXT NOT NULL,
  classification TEXT NOT NULL,
  due_date DATE,
  image_data TEXT, -- Can store base64 or URL
  created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- Create indexes for performance
CREATE INDEX IF NOT EXISTS idx_safety_reports_user_id ON safety_reports(user_id);
CREATE INDEX IF NOT EXISTS idx_safety_non_conformities_report_id ON safety_non_conformities(report_id);

-- Habilitar RLS
ALTER TABLE safety_reports ENABLE ROW LEVEL SECURITY;
ALTER TABLE safety_non_conformities ENABLE ROW LEVEL SECURITY;

-- Políticas de Segurança
CREATE POLICY "Users can manage their own safety reports" ON safety_reports FOR ALL USING (auth.uid() = user_id);
CREATE POLICY "Users can manage their own safety non conformities" ON safety_non_conformities FOR ALL USING (
  EXISTS (SELECT 1 FROM safety_reports WHERE id = safety_non_conformities.report_id AND user_id = auth.uid())
);
