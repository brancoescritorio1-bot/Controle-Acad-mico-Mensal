-- Tabela de Usuários da Chácara
CREATE TABLE IF NOT EXISTS chacara_users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID NOT NULL, -- Referência ao usuário do sistema (dono da chácara)
  name TEXT NOT NULL,
  phone TEXT NOT NULL,
  last_reading NUMERIC DEFAULT 0,
  last_water_reading NUMERIC DEFAULT 0,
  has_energy BOOLEAN DEFAULT TRUE,
  has_water BOOLEAN DEFAULT TRUE,
  energy_active BOOLEAN DEFAULT TRUE,
  water_active BOOLEAN DEFAULT TRUE,
  energy_meters_count INTEGER DEFAULT 1,
  water_meters_count INTEGER DEFAULT 1,
  last_reading_2 NUMERIC DEFAULT 0,
  last_water_reading_2 NUMERIC DEFAULT 0,
  energy_readings JSONB DEFAULT '[]',
  water_readings JSONB DEFAULT '[]',
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Tabela de Contas de Energia da Chácara
CREATE TABLE IF NOT EXISTS chacara_bills (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID NOT NULL, -- Referência ao usuário do sistema
  chacara_user_id BIGINT REFERENCES chacara_users(id) ON DELETE CASCADE,
  month_reference TEXT NOT NULL,
  reading_date DATE NOT NULL,
  due_date DATE NOT NULL,
  prev_reading NUMERIC NOT NULL,
  curr_reading NUMERIC NOT NULL,
  prev_reading_2 NUMERIC DEFAULT 0,
  curr_reading_2 NUMERIC DEFAULT 0,
  kwh_value NUMERIC NOT NULL,
  water_prev_reading NUMERIC DEFAULT 0,
  water_curr_reading NUMERIC DEFAULT 0,
  water_prev_reading_2 NUMERIC DEFAULT 0,
  water_curr_reading_2 NUMERIC DEFAULT 0,
  water_value NUMERIC DEFAULT 0,
  water_service_fee NUMERIC DEFAULT 0,
  apportionment_value NUMERIC DEFAULT 0,
  include_apportionment BOOLEAN DEFAULT TRUE,
  reserve_fund NUMERIC DEFAULT 0,
  total NUMERIC NOT NULL,
  include_reserve_fund BOOLEAN DEFAULT TRUE,
  status TEXT DEFAULT 'pending',
  amount_paid NUMERIC DEFAULT 0,
  payment_observation TEXT,
  energy_readings JSONB DEFAULT '[]',
  water_readings JSONB DEFAULT '[]',
  created_at TIMESTAMPTZ DEFAULT NOW()
);

