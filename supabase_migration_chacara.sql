-- Tabela de Configurações da Chácara
CREATE TABLE IF NOT EXISTS chacara_settings (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID NOT NULL UNIQUE, -- Referência ao usuário do sistema
  default_kwh NUMERIC DEFAULT 0.95,
  default_due_day INTEGER DEFAULT 10,
  default_reading_day INTEGER DEFAULT 5,
  reserve_fund_value NUMERIC DEFAULT 50.00,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

