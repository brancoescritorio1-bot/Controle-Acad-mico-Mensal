-- Políticas de Segurança
CREATE POLICY "Users can manage their own periods" ON periods FOR ALL USING (auth.uid() = user_id);
CREATE POLICY "Users can manage their own subjects" ON subjects FOR ALL USING (auth.uid() = user_id);

-- Para as tabelas relacionadas, a política verifica se o usuário é dono da matéria vinculada
CREATE POLICY "Users can manage their own presencas" ON presencas FOR ALL 
  USING (EXISTS (SELECT 1 FROM subjects WHERE subjects.id = presencas.mes_materia_id AND subjects.user_id = auth.uid()));

CREATE POLICY "Users can manage their own notas" ON notas_atividades FOR ALL 
  USING (EXISTS (SELECT 1 FROM subjects WHERE subjects.id = notas_atividades.mes_materia_id AND subjects.user_id = auth.uid()));

CREATE POLICY "Users can manage their own conteudos" ON conteudos_web FOR ALL 
  USING (EXISTS (SELECT 1 FROM subjects WHERE subjects.id = conteudos_web.mes_materia_id AND subjects.user_id = auth.uid()));
