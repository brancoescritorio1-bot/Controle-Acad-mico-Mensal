-- Tabela de Períodos
CREATE TABLE IF NOT EXISTS periods (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID NOT NULL DEFAULT auth.uid(),
  name TEXT NOT NULL,
  start_date DATE,
  end_date DATE,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Tabela de Matérias (Subjects)
CREATE TABLE IF NOT EXISTS subjects (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID NOT NULL DEFAULT auth.uid(),
  month_year TEXT,
  subject_name TEXT NOT NULL,
  professor TEXT,
  workload NUMERIC,
  period_id BIGINT REFERENCES periods(id) ON DELETE CASCADE,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Tabela de Presenças
CREATE TABLE IF NOT EXISTS presencas (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  mes_materia_id BIGINT REFERENCES subjects(id) ON DELETE CASCADE,
  periodo_id BIGINT REFERENCES periods(id) ON DELETE CASCADE,
  aula_1 BOOLEAN DEFAULT FALSE,
  aula_2 BOOLEAN DEFAULT FALSE,
  aula_3 BOOLEAN DEFAULT FALSE,
  aula_4 BOOLEAN DEFAULT FALSE,
  data_aula_1 DATE,
  data_aula_2 DATE,
  data_aula_3 DATE,
  data_aula_4 DATE,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Tabela de Notas e Atividades
CREATE TABLE IF NOT EXISTS notas_atividades (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  mes_materia_id BIGINT REFERENCES subjects(id) ON DELETE CASCADE,
  periodo_id BIGINT REFERENCES periods(id) ON DELETE CASCADE,
  atividade_1_nota NUMERIC DEFAULT 0,
  atividade_2_nota NUMERIC DEFAULT 0,
  prova_nota NUMERIC DEFAULT 0,
  atividade_1_status TEXT DEFAULT 'Não iniciada',
  atividade_2_status TEXT DEFAULT 'Não iniciada',
  atividade_1_prazo DATE,
  atividade_2_prazo DATE,
  prova_data DATE,
  atividade_1_inicio DATE,
  atividade_2_inicio DATE,
  prova_inicio DATE,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Tabela de Conteúdos Web
CREATE TABLE IF NOT EXISTS conteudos_web (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  mes_materia_id BIGINT REFERENCES subjects(id) ON DELETE CASCADE,
  periodo_id BIGINT REFERENCES periods(id) ON DELETE CASCADE,
  conteudo_1_assistido BOOLEAN DEFAULT FALSE,
  conteudo_2_assistido BOOLEAN DEFAULT FALSE,
  conteudo_3_assistido BOOLEAN DEFAULT FALSE,
  conteudo_4_assistido BOOLEAN DEFAULT FALSE,
  data_1 DATE,
  data_2 DATE,
  data_3 DATE,
  data_4 DATE,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

